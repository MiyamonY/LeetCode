package main

import "fmt"

var graph [][]int

type pos struct {
	x int
	y int
	d int
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

func bfs(kinds [][]int, i, j, kind int) {
	queue := []pos{pos{x: i, y: j}}
	kinds[i][j] = kind

	for len(queue) > 0 {
		top := queue[0]
		queue = queue[1:]
		for _, p := range []pos{{x: -1, y: 0}, {x: 1, y: 0}, {x: 0, y: -1}, {x: 0, y: 1}} {
			x := top.x + p.x
			y := top.y + p.y
			if 0 <= x && x < len(graph) && 0 <= y && y < len(graph[0]) && graph[x][y] == 1 && kinds[x][y] == 0 {
				kinds[x][y] = kind
				queue = append(queue, pos{x: x, y: y})
			}
		}
	}

}

func dfs2(kinds [][]int, i, j int) (ans int) {
	ans = 1 << 60
	visited := make([][]bool, len(kinds))
	for i := range visited {
		visited[i] = make([]bool, len(kinds[i]))
	}

	visited[i][j] = true
	queue := []pos{pos{i, j, 0}}
	for len(queue) > 0 {
		top := queue[0]
		queue = queue[1:]
		for _, p := range []pos{{x: -1, y: 0}, {x: 1, y: 0}, {x: 0, y: -1}, {x: 0, y: 1}} {
			x := top.x + p.x
			y := top.y + p.y
			dist := top.d + 1
			if 0 <= x && x < len(kinds) && 0 <= y && y < len(kinds[0]) && !visited[x][y] {
				visited[x][y] = true
				if kinds[x][y] != 2 {
					queue = append(queue, pos{x: x, y: y, d: dist})
				} else {
					ans = min(ans, dist)
				}
			}
		}
	}
	return
}

func shortestBridge(A [][]int) (ans int) {
	kinds := make([][]int, len(A))
	for i := range kinds {
		kinds[i] = make([]int, len(A[0]))
	}
	graph = A

	kind := 1
	for i := range A {
		for j := range A[i] {
			if A[i][j] == 1 && kinds[i][j] == 0 {
				bfs(kinds, i, j, kind)
				kind++
			}
		}
	}

	ans = 1 << 60
	for i := range A {
		for j := range A[i] {
			if kinds[i][j] == 1 {
				ans = min(ans, dfs2(kinds, i, j))
			}
		}
	}
	return ans - 1
}

