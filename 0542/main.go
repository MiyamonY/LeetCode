func wfs(matrix [][]int, dists [][]int) {
	visited := make([][]bool, len(dists))
	for k := range visited {
		visited[k] = make([]bool, len(dists[0]))
	}

	var fifo []pos
	for i, _ := range matrix {
		for j, val := range matrix[i] {
			if val == 0 {
				fifo = append(fifo, pos{i, j})
				dists[i][j] = 0
			}
		}
	}

	for len(fifo) > 0 {
		top := fifo[0]
		fifo = fifo[1:]
		for _, d := range []pos{{1, 0}, {-1, 0}, {0, 1}, {0, -1}} {
			s := top.x + d.x
			t := top.y + d.y
			if 0 <= s && s < len(matrix) && 0 <= t && t < len(matrix[0]) && matrix[s][t] == 1 && !visited[s][t] && dists[top.x][top.y]+1 < dists[s][t] {
				dists[s][t] = dists[top.x][top.y] + 1
				fifo = append(fifo, pos{s, t})
			}
		}
	}
}

func updateMatrix(matrix [][]int) [][]int {
	dists := make([][]int, len(matrix))
	for i := range dists {
		dists[i] = make([]int, len(matrix[0]))
		for j, _ := range dists[i] {
			dists[i][j] = 1 << 30
		}
	}

	wfs(matrix, dists)
	return dists
}

